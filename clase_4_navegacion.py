# Definimos algunas politicas arbitrarias que podrían ser utilizadas para inicializar el algoritmo.
def PoliticaEsteNorte():
  U = Tabla(Indice(IndiceEstados, Estados), Acciones)
  U[Acciones] = 0
  for x in U.index:
    if x!=(Ncolumnas-1,Nfilas-1):
      if AccionValida(x,'E'):
        U.loc[x,'E'] = 1
      else:
        U.loc[x,'N'] = 1
  return U

def PoliticaNorteEste():
  U = Tabla(Indice(IndiceEstados, Estados), Acciones)
  U[Acciones] = 0
  for x in U.index:
    if x!=(Ncolumnas-1,Nfilas-1):
      if AccionValida(x,'N'):
        U.loc[x,'N'] = 1
      else:
        U.loc[x,'E'] = 1
  return U

def IteraciondePolitica(Ambiente, Politica0 = None, epsilon = 1e-6):
  # Usamos esa función para convertir de política estocástica a determinística.
  def NombreAccion(Politica):
    return np.array(Acciones)[np.argmax(Politica, axis = 1)]

  # Inicializamos la politica inicial = Politica0
  if Politica0 is None:
    Politica0 = PoliticaEsteNorte()
  un = Politica0

  # Vamos a guardar los resultados intermediarios de Iteración de Política en Un. Ese paso no es esencial.
  Un = Tabla(Politica0.index)
  Un[('U', 0)] = NombreAccion(un) # Guardamos acciones por sus nombres: N, S, E, O.
  Un.columns = pd.MultiIndex.from_tuples(Un.columns)

  # evaluación de la política (iteración de valor con política fija)
  Pablo, Vn = IteraciondeValor(Ambiente,un)
  Un[('V', 0)] = Pablo.V
  n = 0
  while True:
    # actualización de la política
    un = Ambiente.PoliticaCodiciosa(Pablo.V)
    # evaluación de la política
    PabloPlus, Vn = IteraciondeValor(Ambiente,un)
    if np.max(np.abs(PabloPlus.V-Pablo.V))<epsilon: # terminamos cuando la nueva politica no mejora la función valor
      break
    else:
      n = n+1
      Pablo = PabloPlus
      Un[('U', n)] = NombreAccion(un)
      Un[('V', n)] = Pablo.V
  return Pablo, Un

