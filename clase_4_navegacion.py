# Calcular la función valor V_u de la política u, o la función valor óptima V^* si politica = None
def IteraciondeValor(Ambiente, Politica = None, epsilon = 1e-6):
  Pablo = Agente()
  Pablo.V = 0
  Vn = pd.DataFrame(Pablo.V)
  Vn.columns = [0]
  n = 0
  while True:
    # iteramos la función valor
    Vp = Ambiente.IteraciondeValor(Pablo.V, Politica)
    # Testamos si V ha convergido (diferencia entre V y Vp suficientemente pequeña)
    if np.max(np.abs(Vp-Pablo.V))<epsilon:
      break
    else:
      Pablo.V = Vp
      n = n+1
      Vn[n] = Vp

  # Especificamos la política que corresponde a la función valor. Si no se especificó la política, esa será la política óptima, que es codiciosa con respecto a V^*.
  if Politica is None:
    Pablo.U = Ambiente.PoliticaCodiciosa(Pablo.V) # la politica del agente es codiciosa con respecto a la funcion valor
  else:
    Pablo.U = Politica
  return Pablo, Vn

