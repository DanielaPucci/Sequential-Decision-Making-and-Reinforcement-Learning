def PoliticaQ(Q):
  # Devuelve la política a que minimiza Q(x,a) para cada estado x
  return Q.loc[Q.groupby([('x',0),('x',1)], axis = 0).V.idxmin()]

def QLearning(Ambiente, epsilon, N = 1000):

  Qhat = Tabla(Indice(IndiceEstadosAcciones, EstadosAcciones), ['count','V'])
  Qhat[:] = 0
  U_aleatoria = PoliticaAleatoria() #utilizamos la política aleatoria para agregar exploración

  for n in range(N):
    print(n, end = ' ')
    # el ambiente tiene tiempos nuevos (a partir de la misma distribución a cada episodio/día)
    Ambiente.GenerarTiempoReal()
    x = (0,0) # empezamos en el estado (0,0), pero también sería posible empezar en un estado aleatorio: random.choices([(columna,fila) for fila in range(Nfilas) for columna in range(Ncolumnas)])[0]
    while not np.array_equal(x,[Ncolumnas-1,Nfilas-1]):
      # calculamos la acción que es epsilon-greedy, equilibrando exploración y explotación
      Ux = epsilon*U_aleatoria.loc[x]
      Ux[Qhat.loc[x,'V'].idxmin()] += 1-epsilon
      # la acción que se elige es aleatorizada, con más probabilidad a la acción greedy.
      a = random.choices(Acciones, Ux)[0]
      # simulamos una transición en el ambiente, recibiendo el próximo estado xp y la recompensa r
      r, xp = Ambiente.Transicion_TiempoReal(x,a)
      # xa es el par estado-acción (x,a). Actualizamos el valor de Qhat(x,a) utilizando la observación de r y xp
      xa = x+(a,)
      Qhat.loc[xa,'count'] = Qhat.loc[xa,'count']+1
      # usamos velocidad de aprendizaje = 1/Qhat.loc[xa,'count'] = 1/N_{x,a}
      Qhat.loc[xa,'V'] = Qhat.loc[xa,'V']+(r+min(Qhat.loc[xp,'V'])-Qhat.loc[xa,'V'])/Qhat.loc[xa,'count']
      x = xp
      #print(x,a, xp)
      #print(Qhat.loc[x])

  return Qhat

