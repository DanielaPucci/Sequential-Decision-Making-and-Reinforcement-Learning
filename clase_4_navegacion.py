# La clase Mapa contiene parámetros y funciones que definen el comportamiento del ambiente.
# Tenemos tres funciones esenciales y parámetros y funciones auxiliares que las acompañan:
  # Función esencial: Para todos los estados, calcula Vp(x) = min_a{ r_a(x)+E_y[V(y)|x,a]}
  # o Vp(x) = r_u(x)+E_y[V(y)|x,u(x)] si la politica u es especificada.
  # IteraciondeValor(self,V, Politica = None) -> Vp(x)

  # Función esencial: calcula la política codiciosa con respecto a la función valor V, dada por u(x) = arg min_a{ r_a(x)+E_y[V(y)|x,a]}
  # PoliticaCodiciosa(self, V) -> U

  # Función esencial: genera una muestra de recompensa r y próximo estado xp a partir de estado x in acción a.
  # Transicion_TiempoReal(self, x,a) -> (r, xp)
  # Transicion_TiempoReal es la única función que el agente puede utilizar en la situación de aprendizaje por refuerzo.
  # Las otras funciones presuponen conocimiento de las recompensas esperadas y matriz de transición.

class Mapa(object):
  def __init__(self, Tiempo, Penalidad = 500):
    self._Tiempo = Tiempo.copy()
    self._Penalidad = Penalidad*max(self._Tiempo['Esperado']+self._Tiempo['Rango'])
    self.GenerarTiempoReal()

  # Función auxiliar, se utiliza a cada día para tener una nueva muestra de los tiempos de viaje.
  def GenerarTiempoReal(self):
    # Percorrer cada calle requiere un tiempo distribuido uniformemente entre TiempoMinimo y TiempoMaximo
    self._Tiempo['Real'] = self._Tiempo['Esperado']+np.random.uniform(-self._Tiempo['Rango'],self._Tiempo['Rango']+1)

  # Función auxiliar, devuelve el tiempo esperado de viaje para cada calle.
  def TiempoEsperado(self):
    return self._Tiempo['Esperado']

  # Función auxiliar, utilizada para crear transiciones con el tiempo promedio o el tiempo real de transitar una calle.
  def _transicion(self,x,a,Tiempos):
    if ~AccionValida(x,a): # acción lleva a salir del grid
      xp = x
      r = self._Penalidad
    else:
      xp = tuple(np.array(x,dtype = int)+Accion_Movimiento[a])
      direccion = int(a in ['O','E']) # 0 si 'N', 'S', 1 si 'O', 'E'
      if a in ['O','S']: # oeste o norte
        calle = xp+x
      else: # este o norte
        calle = x+xp
      r = Tiempos[calle]
    return(r,xp)

  # Función auxiliar, utilizada en las funciones esenciales abajo. Devuelve el tiempo promedio y el próximo estado a partir de un estado x y acción a.
  def Transicion_TiempoEsperado(self,x,a):
    return self._transicion(x,a,self._Tiempo['Esperado'])

  # Función esencial: Para todos los estados, calcula Vp(x) = min_a{ r_a(x)+E_y[V(y)|x,a]}, o Vp(x) = r_u(x)+E_y[V(y)|x,u(x)] si la politica u es especificada.
  def IteraciondeValor(self,V, Politica = None):
    Vp = V.copy()
    for x in V.index:
      if x!=(Ncolumnas-1,Nfilas-1):
          # Cómo cambiaria esta parte si la transición de estado no fuera determinística?
          valores_acciones = np.array([r+V[xp] for (r,xp) in [self.Transicion_TiempoEsperado(x,a) for a in Acciones]])
          if Politica is None:
            Vp[x] = min(valores_acciones)
          else:
            Vp[x] = np.sum(Politica.loc[x]*valores_acciones)

    return Vp

  # Función esencial: calcula la política codiciosa con respecto a la función valor V, dada por u(x) = arg min_a{ r_a(x)+E_y[V(y)|x,a]}
  def PoliticaCodiciosa(self,V):
    U = Tabla(Indice(IndiceEstados, Estados), Acciones)
    U[['N','S','O','E']] = np.zeros((len(U),4))
    for x in V.index:
      if x!=(Ncolumnas-1,Nfilas-1):
          # Cómo cambiaria esta parte si la transición de estado no fuera determinística?
          valores_acciones = np.array([r+V.loc[xp] for (r,xp) in [self.Transicion_TiempoEsperado(x,a) for a in Acciones]])
          U.loc[x, Acciones[np.argmin(valores_acciones)]] = 1
    return U

  # Función esencial: genera una muestra de recompensa r y próximo estado xp a partir de estado x in acción a.
  def Transicion_TiempoReal(self,x,a):
    return self._transicion(x,a,self._Tiempo['Real'])

