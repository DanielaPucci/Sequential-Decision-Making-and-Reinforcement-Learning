# Evaluación de política por simulación Monte Carlo

ep_index = pd.MultiIndex(levels=[[]]*2, codes=[[]]*2, names=['episodio', 'periodo'])
ep_columns = pd.MultiIndex.from_tuples([('x',0),('x',1),('a','.'),('r','.'),('x+',0),('x+',1)])

# Definimos una politica aleatoria, que toma todas las acciones válidas con la misma probabilidad.
def PoliticaAleatoria():
  U = Tabla(Indice(IndiceEstados, Estados), Acciones)
  U[['N','S','O','E']] = np.zeros((len(U),4))
  for x in U.index:
    for accion in Acciones:
      if AccionValida(x,accion):
        U.loc[x,accion] = 1
    U.loc[x] = U.loc[x]/np.sum(U.loc[x])
  return U

# Esta función genera una trayectoria a partir del estado x0, siguiendo la política especificada, hasta llegar al estado final.
# El parámetro n es el número del episodio.
def episodio(n, Ambiente, Politica = None, x0 = None):
  # si la política no está especificada, utilizamos la política aleatoria
  if Politica is None:
    Politica = PoliticaAleatoria()
  ep = Tabla(ep_index, ep_columns)
  # si el estado inicial no está especificado, utilizamos (0,0)
  if x0 is None:
    x = (0, 0)
  else:
    x = x0
  p = 0
  while not np.array_equal(x,[Ncolumnas-1,Nfilas-1]): # mientras no estamos en el estado final
    a = random.choices(Acciones, Politica.loc[x])[0] # elegir una accion entre Acciones con distribución Politica.loc[x]
    r, xp = Ambiente.Transicion_TiempoReal(x,a) # obtenemos una muestra del próximo estado xp y recompensa r dado estado actual x y acción a
    ep.loc[(n,p),:] = np.hstack([x,a,r,xp], dtype=object) # ponemos la muestra completa (estado, acción, recompensa, estado siguiente) en el episodio
    x = xp
    p = p+1
  ep['v'] = np.cumsum(ep['r'][::-1])[::-1] # calculamos el tiempo total en el episodio que llevó ir de cada posición al destino
  return ep


def MonteCarlo(Ambiente, N, Politica = None, x0 = None):

  episodios = Tabla(ep_index, ep_columns)
  Vhatn_MC = Tabla(Indice(IndiceEstados, Estados), ['count']+list(range(N+1)))
  Vhatn_MC.loc[:, :] = 0

  if Politica is None:
    Politica = PoliticaAleatoria()

  for n in range(N):
    # empezamos en un estado aleatorio; también sería posible empezar siempre en x0 = (0,0).
    if x0 is None:
      x = random.choices([(columna,fila) for fila in range(Nfilas) for columna in range(Ncolumnas)])[0]
    else:
      x = x0
    if (n%100)==0:
      print(n, end = ' ')
    # generamos los tiempos para el episodio n
    Ambiente.GenerarTiempoReal()
    episodios = pd.concat([episodios,episodio(n, Ambiente, Politica, x)])

    # estimativa de la función valor por Monte Carlo: V(x) es el tiempo promedio de ir del estado x al estado final en los episodios
    Vhat = episodios.loc[:,['x','v']].groupby(by = [('x',0),('x',1)]).mean()
    # la función valor en el estado final es 0:
    Vhat.loc[(Ncolumnas-1, Nfilas-1),'v'] = 0
    # guardamos el número de visitas a cada estado y la estimativa de la función valor:
    counts = episodios.loc[:,['x','v']].groupby(by = [('x',0),('x',1)]).count()
    Vhatn_MC[n+1] = Vhatn_MC[n]
    Vhatn_MC.loc[Vhat.index,n+1] = Vhat['v'].values
    Vhatn_MC.loc[counts.index,'count'] = counts['v'].values

  return Vhatn_MC

# Evaluación de política por simulación Monte Carlo con bootstrapping

def MC_Bootstrapping(Ambiente,  N, Politica = None, x0 = None):

  if Politica is None:
    Politica = PoliticaAleatoria()
  Vhatn_BS = Tabla(Politica.index, ['count']+list(range(N+1)))
